#!/usr/bin/env python3
"""Concatenates two arrays."""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Args:
        arr1 (list): The first array.
        arr2 (list): The second array.

    Returns:
        list: A new array that is the concatenation of arr1 and arr2.
    """
    return arr1 + arr2
